

const openT1 = document.getElementById('openT1');
const modalT1 = document.querySelector('.top_modal1');
const overlayT1 = modalT1.querySelector('.top_overlay1');
const closeBtnT1 = modalT1.querySelector('.buttonT1');

const openModalT1 = () => {
    modalT1.classList.remove('top_hidden1');
}
const closeModalT1 = () => {
    modalT1.classList.add('top_hidden1');
}
overlayT1.addEventListener('click', closeModalT1);
closeBtnT1.addEventListener('click', closeModalT1);
openT1.addEventListener('click', openModalT1);



